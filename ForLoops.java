public class ForLoops {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++){
            if (i % 2 == 0){
                System.out.println(i+ " is multiple of 2");
            }
        }

        for (int i = 0; i < 10; i++){
            System.out.println("Hello there  " + i);
        }
    }
}